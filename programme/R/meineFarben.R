meineFarben <- function(n){
  Red   <- c("0xFF","0xFF","0xFF","0xFF","0xFF","0xFF","0xEF","0xDD","0xCC","0xC1","0xB8","0xB1")
  Green <- c("0x00","0x80","0xA9","0xCA","0xE1","0xF2","0xEF","0xE5","0xD9","0xD3","0xCD","0xC8")
  Blue  <- c("0x14","0x19","0x5B","0x96","0xC7","0xF0","0xFF","0xFF","0xFF","0xFF","0xFF","0xFF")
  
  Red   <- c("0xB1","0xB8","0xC1","0xCC","0xCC","0xDD","0xEF","0xFF","0xFF","0xFF","0xFF","0xFF")
  Green <- c("0xC8","0xCD","0xD3","0xD9","0xE5","0xEF","0xF2","0xE1","0xCA","0xA9","0x80","0x00")
  Blue  <- c("0xFF","0xFF","0xFF","0xFF","0xFF","0xFF","0xF0","0xC7","0x96","0x5B","0x19","0x14")
  
#  Red   <- c("0x00","0xB1","0xF5","0xFF")
#  Green <- c("0x4B","0xDA","0xC7","0x01")
#  Blue  <- c("0x95","0xF1","0x91","0x19")
  
  fr <- splinefun(1:length(Red), strtoi(Red))
  fg <- splinefun(1:length(Green), strtoi(Green))
  fb <- splinefun(1:length(Blue), strtoi(Blue))
  result <- rep(NA,n)
  for (i in 1:n){
    ii <- 1+(i-1)*(length(Red)-1)/(n-1)

    rint <- round(fr(ii),digits=0)
    rint <- min(max(0,rint),255)
    rhex <- as.hexmode(rint)
    if (rint<16) {rhex <- paste("0",rhex,sep="")}
    else if (rint == 0) {rhex <- "00"}
    gint <- round(fg(ii),digits=0)
    gint <- min(max(0,gint),255)
    ghex <- as.hexmode(gint)
    if (gint<16) {ghex <- paste("0",ghex,sep="")}
    else if (gint == 0) {ghex <- "00"}
    bint <- round(fb(ii),digits=0)
    bint <- min(max(0,bint),255)
    bhex <- as.hexmode(bint)
    if (bint<16) {bhex <- paste("0",bhex,sep="")}
    else if (bint == 0) {bhex <- "00"}
    result[i] <- paste("#",rhex,ghex,bhex,sep="")
  }
  return(result)
}